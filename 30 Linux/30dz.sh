# !/bin/bash

# --
# Скрипт 30dz.sh
# Домашнее задание по теме 30
# Lite:
# Подготовить bash-скрипт, который автоматизирует типичную задачу:
# 1. Установка окружения,
# 2. Загрузка данных,
# 3. Запуск аналитического Python-скрипта.
# Записать скрипт в один файл (.py или.txt) и прислать в качестве ответа.

# Указываем "строгий" режим:
# -e  скрипт будет завершаться при ошибке любой команды
# -u  использование неинициализированных переменных считается ошибкой
# -o pipefail  если команда внутри пайпа завершается с ошибкой, весь пайп фейлится
set -euo pipefail

# 0. (Опционально) Настройки
PYTHON=python3    # Можно переопределить, если хотим использовать другой интерпретатор
REQUIREMENTS_FILE="requirements.txt"   # Файл зависимостей (если есть)
MAIN_SCRIPT="main.py"                  # Python-скрипт, который будем запускать

# 1. Проверяем, есть ли папка venv
if [ ! -d "venv" ]; then
    echo "[INFO] Виртуальное окружение (venv) не найдено. Создаём заново..."

    # 2. Создаём окружение
    $PYTHON -m venv venv

    echo "[INFO] Окружение создано. Устанавливаем необходимые пакеты..."

    # Активируем окружение для установки
    source venv/bin/activate

    # 2a. Проверяем, есть ли файл с зависимостями. Если нет, пропускаем
    if [ -f "$REQUIREMENTS_FILE" ]; then
        pip install --upgrade pip
        pip install -r "$REQUIREMENTS_FILE"
    else
        echo "[WARN] Файл $REQUIREMENTS_FILE не найден. Пропускаем установку зависимостей..."
    fi

    # Деактивируем окружение, чтобы финальный запуск был понятен
    deactivate

else
    echo "[INFO] Виртуальное окружение (venv) уже существует."
fi

# Загружаем данные
data_my_ip=`curl -s ifconfig.me`
echo "Ваш IP адрес: $data_my_ip"

# 3. Запускаем Python-скрипт, активировав окружение
echo "[INFO] Активируем окружение и запускаем $MAIN_SCRIPT..."
source venv/bin/activate

# Если main.py требует аргументов, можно передавать их через $@
# Но предположим, что мы просто запускаем его без аргументов.
if [ -f "$MAIN_SCRIPT" ]; then
    python "$MAIN_SCRIPT"
else
    echo "[ERROR] Файл $MAIN_SCRIPT не найден!"
    exit 1
fi

# (Опционально) после завершения скрипта отключить окружение
deactivate

echo "[INFO] Скрипт завершён успешно."
