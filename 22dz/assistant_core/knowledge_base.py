knowledge_index: dict[str, str] = {
    "tariff": (
        "Наш ИИ-ассистент помогает сотрудникам быстро разбираться в тарифах и услугах. "
        "Он умеет объяснять различия между тарифными планами простым языком, "
        "подсказывать, какой план подойдёт клиенту в типичных сценариях."
    ),
    "support": (
        "Ассистент обучен на внутренних скриптах службы поддержки. "
        "Он знает стандартные шаги при обращении клиента, "
        "умеет задавать уточняющие вопросы и подбирать корректные формулировки ответов."
    ),
    "limits": (
        "В базе знаний описаны основные лимиты по продуктам: "
        "ограничения по количеству запросов, срокам хранения данных и размеру файлов. "
        "Ассистент может опираться на эти данные, чтобы давать точные ответы."
    ),
    "refund": (
        "Раздел по возвратам содержит алгоритм действий при запросе клиента на возврат средств: "
        "какие данные нужно уточнить, в каких случаях возврат возможен, а когда нет."
    ),
}


fallback_knowledge: str = (
    "Пока в базе знаний нет прямого ответа на этот вопрос. "
    "Используй общие знания модели, но формулируй ответ аккуратно и честно."
)


def get_knowledge_snippet(question: str) -> str:
    question_lower: str = question.lower()

    for keyword, knowledge_text in knowledge_index.items():
        keyword_lower: str = keyword.lower()

        if keyword_lower in question_lower:
            return knowledge_text

    return fallback_knowledge